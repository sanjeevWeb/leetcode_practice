// Function to find square root
// x: element to find square root
class Solution{
  public:
    long long int floorSqrt(long long int x) 
    {
        // Your code goes here  
        if(x == 1 || x == 2) return 1;
        long long int val;
        for(long long int i=1;i<x;i++){
            if((i*i) > x){
                val = i;
                break;
            }
        }
        return val-1;
    }
};

